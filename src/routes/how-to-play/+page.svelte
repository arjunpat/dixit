		<h1 class="text-3xl">Talespin</h1>
		<br />
		<!-- <a href="/" class="link underline">Start playing</a>
		<br /><br /> -->
		<p>
			Talespin (inspired by <a
				href="https://en.wikipedia.org/wiki/Dixit_(board_game)"
				class="link underline">Dixit</a
			>) is a simple-to-learn card game that emphasizes creativity and abstract thinking. Here's how
			to play:
		</p>
		<br />

		<p><strong>Number of Players:</strong> 3-8 (best with 4-6)</p>
		<br />

		<p>
			<strong>Objective:</strong> To earn the most points by correctly guessing the storyteller's card
			and deceiving others into choosing the wrong card.
		</p>
		<br />

		<strong class="text-xl underline">Game Play</strong>

		<p>
			1. <strong>Cards dealt:</strong> All players are dealt six cards with whimsical, dreamlike images.
		</p>
		<p>
			2. <strong>Storytelling:</strong> One player is chosen as the storyteller for the round. The storyteller
			selects a card from their hand and gives a clue about it. This clue can be a word, phrase, or a
			sentence that somehow describes the card's image.
		</p>
		<p>
			3. <strong>Card Selection:</strong> After hearing the clue, the other players select one of their
			own cards that best matches the storyteller's clue, showing it to nobody.
		</p>
		<p>
			4. <strong>Display and Voting:</strong> The chosen cards all laid out face up. Players (except
			the storyteller) then use their voting tokens to guess which card belongs to the storyteller.
		</p>
		<p>
			5. <strong>End of Round:</strong> layers draw cards to refill their hands to 6. The role of the
			storyteller passes to the next player.
		</p>

		<br />

		<strong class="text-xl underline">Scoring</strong>

		<p>
			• If all or none of the players find the storyteller's card, the storyteller scores 0, and
			each of the other players scores 2 points.
		</p>
		<p>
			• Otherwise, the storyteller and whoever found the storyteller's card each score 3 points.
		</p>
		<p>
			• Players (not including the storyteller) score 1 point for each vote their own cards receive.
		</p>
		<br />
		<strong class="text-xl underline">Strategies</strong>

		<p>
			• The storyteller's clue should be obscure enough so that not everyone can guess the correct
			card, but not so obscure that no one can guess it.
		</p>
		<p>
			• When selecting a card in response to the storyteller's clue, choose one that could match the
			clue in an unexpected way to increase the chances of misleading other players.
		</p>

