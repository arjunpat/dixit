		<p>
			Talepsin (inspried by <a
				href="https://en.wikipedia.org/wiki/Dixit_(board_game)"
				class="link underline">Dixit</a
			>) is a captivating, simple-to-learn card game that emphasizes creativity and abstract
			thinking. Here's how to play:
		</p>
		<br />

		<p><strong>Number of Players:</strong> 3-8 (best with 4-6)</p>
		<br />

		<p>
			<strong>Objective:</strong> To earn the most points by correctly guessing the storyteller's card
			or having others guess your card when you are the storyteller.
		</p>
		<br />

		<strong>Game Play:</strong>

		<p>
			1. <strong>Storytelling:</strong> One player is chosen as the storyteller for the round. The storyteller
			selects a card from their hand and gives a clue about it. This clue can be a word, phrase, or a
			sentence that somehow describes the card's image.
		</p>
		<p>
			2. <strong>Card Selection:</strong> After hearing the clue, the other players select one of their
			own cards that best matches the storyteller's clue, showing it to nobody.
		</p>
		<p>
			3. <strong>Display and Voting:</strong> The cards received cards, including their own, and lays
			them out face up. Players (except the storyteller) then use their voting tokens to guess which
			card belongs to the storyteller.
		</p>

		<br />

		<strong>Scoring:</strong>

		<p>
			If all or none of the players find the storyteller's card, the storyteller scores 0, and each
			of the other players scores 2 points. Otherwise, the storyteller and whoever found the
			storyteller's card each score 3 points. Players (not including the storyteller) score 1 point
			for each vote their own cards receive.
		</p>

